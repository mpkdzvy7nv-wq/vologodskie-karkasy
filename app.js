function formatPrice(value) {
  return value == null ? 'по запросу' : new Intl.NumberFormat('ru-RU').format(value) + ' ₽';
}
function getProductPrice(item) {
  if (item.variants && item.variants.length) return item.variants[0].price ?? item.price;
  return item.price;
}
function getCategoryProducts(category) {
  return category === 'Все' ? products : products.filter(item => item.category === category);
}
function minCategoryPrice(category) {
  const values = getCategoryProducts(category).flatMap(item => {
    const variantPrices = (item.variants || []).map(v => v.price).filter(v => v != null);
    return variantPrices.length ? variantPrices : [item.price].filter(v => v != null);
  });
  return values.sort((a, b) => a - b)[0];
}
function getAvailableTypes() {
  return ['Все виды', ...new Set(getCategoryProducts(activeCategory).map(item => item.type).filter(Boolean))];
}
function renderFeaturedCategories() {
  const featured = categories.filter(cat => cat !== 'Все');
  featuredEl.innerHTML = featured.map(category => {
    const info = categoryInfo[category] || { icon: '📌', text: 'Товары для дачи и участка.' };
    const count = getCategoryProducts(category).length;
    const minPrice = minCategoryPrice(category);
    return `<article class="direction-item" data-category="${category}" role="button" tabindex="0" aria-label="Открыть категорию ${category}">
      <div class="direction-icon">${info.icon}</div>
      <b>${category}</b>
      <span>${info.text}</span>
      <strong>${minPrice ? `от ${formatPrice(minPrice)}` : 'цена по запросу'} • ${count} поз.</strong>
    </article>`;
  }).join('');
}
function renderTabs() {
  tabsEl.innerHTML = categories.map(category => `<button class="tab ${category === activeCategory ? 'active' : ''}" type="button" data-category="${category}">${category}</button>`).join('');
}
function renderTypeTabs() {
  const types = getAvailableTypes();
  if (activeCategory === 'Все') { typeTabsEl.innerHTML = ''; return; }
  if (!types.includes(activeType)) activeType = 'Все виды';
  typeTabsEl.innerHTML = types.map(type => `<button class="type-tab ${type === activeType ? 'active' : ''}" type="button" data-type="${type}">${type}</button>`).join('');
}
function getFilteredProducts() {
  const query = searchInput.value.trim().toLowerCase();
  const filtered = products.filter(item => {
    const categoryMatch = activeCategory === 'Все' || item.category === activeCategory;
    const typeMatch = activeType === 'Все виды' || item.type === activeType;
    const variantsText = (item.variants || []).map(v => `${v.label} ${formatPrice(v.price)}`).join(' ');
    const text = [item.name, item.category, item.type, item.description || '', ...item.specs, item.badge, variantsText].join(' ').toLowerCase();
    return categoryMatch && typeMatch && (!query || text.includes(query));
  });
  if (activeCategory !== 'Все') {
    filtered.sort((a, b) => {
      const ta = typeOrder.includes(a.type) ? typeOrder.indexOf(a.type) : 999;
      const tb = typeOrder.includes(b.type) ? typeOrder.indexOf(b.type) : 999;
      return ta - tb || a.id - b.id;
    });
  }
  return filtered;
}
function renderMeta(count) {
  const suffix = activeCategory === 'Все' ? 'во всём каталоге' : `в разделе «${activeCategory}»`;
  catalogMeta.textContent = `Показано ${count} ${suffix}`;
}
function cleanProductName(item) {
  return item.name.replace(/^Каркас\s+/, '').replace(/^Летний душ\s+/, '').trim();
}
function descriptionIntro(item) {
  if (item.description) return item.description;
  const n = cleanProductName(item).replace(/[«»]/g, '');
  return `${n} — товар для дачи и участка.`;
}
function productDetails(item) {
  const variants = item.variants && item.variants.length ? item.variants.map(v => `${v.label}: ${formatPrice(v.price)}`).join('; ') : '';
  const blocks = [];
  blocks.push(`<p>${item.detailLead || descriptionIntro(item)}</p>`);
  const detailCells = [`<div><b>Характеристики</b><span>${(item.specs || []).join('; ')}.</span></div>`];
  if (variants) detailCells.push(`<div><b>Размеры и варианты</b><span>${variants}</span></div>`);
  blocks.push(`<div class="details-grid">${detailCells.join('')}</div>`);
  blocks.push(`<p>Для точного подбора можно уточнить наличие, покрытие, доставку, монтаж и дополнительные элементы.</p>`);
  return `<div class="product-details" aria-hidden="true"><div class="details-inner">${blocks.join('')}</div></div>`;
}
function variantButtons(item) {
  if (!item.variants || !item.variants.length) return '';
  return `<div class="variant-block"><small>Размер / вариант:</small><div class="variant-options">${item.variants.map((variant, index) => `<button type="button" class="variant-option ${index === 0 ? 'active' : ''}" data-price="${variant.price ?? ''}" data-label="${variant.label}">${variant.label}</button>`).join('')}</div></div>`;
}
function renderProducts() {
  const filtered = getFilteredProducts();
  renderMeta(filtered.length);
  if (!filtered.length) { gridEl.innerHTML = '<div class="empty">Ничего не найдено. Попробуйте другой запрос или откройте вкладку «Все» в конце списка.</div>'; return; }
  let currentType = null; let html = '';
  filtered.forEach(item => {
    if (activeCategory !== 'Все' && item.type !== currentType) { currentType = item.type; html += `<div class="catalog-group-title"><span>${currentType}</span></div>`; }
    const defaultVariant = item.variants?.[0];
    const price = defaultVariant?.price ?? item.price;
    html += `<article class="product-card" data-category="${item.category}" data-details-card="true">
      <div class="product-visual ${item.image ? "has-photo" : ""}">${item.image ? `<img src="${item.image}" alt="${item.name}" loading="lazy" onerror="this.closest('.product-visual').classList.add('image-failed'); this.remove();">` : ""}<span class="badge">${item.badge}</span><span class="type-badge">${item.type}</span></div>
      <div class="product-content">
        <h3>${item.name}</h3>
        <p class="product-lead">${item.description || ""}</p>
        <ul class="specs">${item.specs.map(spec => `<li>${spec}</li>`).join("")}</ul>
        ${variantButtons(item)}
        <button class="details-toggle" type="button" aria-expanded="false">Подробнее о товаре</button>
        ${productDetails(item)}
        <div class="price-row"><div class="price"><small>стоимость</small><strong data-price-text>${formatPrice(price)}</strong>${defaultVariant ? `<em data-variant-text>${defaultVariant.label}</em>` : ""}</div><a class="mini-cta" href="https://wa.me/79000000000" target="_blank" rel="noopener">Уточнить</a></div>
      </div>
    </article>`;
  });
  gridEl.innerHTML = html;
}
